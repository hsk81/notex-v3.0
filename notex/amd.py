import subprocess

###############################################################################
###############################################################################

def optimize(amd_conf, amd_opts={
        'optimizer': './static/js/lib/require/r-2.3.6.js',
        'app-path': './static/build/app.js'
    }):

    subprocess.call([
        "/usr/bin/env", "node", amd_opts['optimizer'], "-o", amd_conf])

    app_path = amd_opts['app-path']
    assert app_path
    return app_path


interface BufferedFunction extends Function {
    cancel:Function;
}

export function buffered(fn:Function, ms:number = 200):BufferedFunction {
    let id:number, gn:Function = function () {
        let self = this, args = arguments;
        if (id !== undefined) {
            clearTimeout(id);
            id = undefined;
        }

        if (id === undefined) {
            id = setTimeout(function () {
                fn.apply(self, args);
                id = undefined;
            }, ms);
        }
    };

    (<BufferedFunction>gn).cancel = function () {
        if (id !== undefined) {
            clearTimeout(id);
            id = undefined;
        }
    };

    return (<BufferedFunction>gn);
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

export default buffered;

